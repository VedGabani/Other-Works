# Convert Temperature Celsius to Fahrenheit and Fahrenheit to Celsius

print("----- Celsius to Fahrenheit -----\n")

temp1 = int(input("Enter Temperature in Celsius -_- "))
temp2 = int(input("Enter Temperature in Fahrenheit -_- "))

a = (temp1 * 1.8)+32
b = (temp2 - 273.15)

print("\n")
print("Temperature in Fahrenheit -_- ", a)
print("Temperature in Celsius", b)
